    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
     
     String did =txtDocId.getText();
     String dname =txtDocName.getText();
     String dspec =txtDocspec.getText();
     
     try{
         Class.forName("com.mysql.jdbc.Driver");
         Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital", "root", "");
         String sql ="update `doctor_reccord` SET `DoctorName`='"+dname+"',`Specialization`='"+dspec+"' WHERE id='"+did+"'";
         PreparedStatement ptst= conn.prepareStatement(sql);
         ptst.execute();
         
         JOptionPane.showMessageDialog(null, "Data Updated Successfilly");
         txtDocId.setText("");txtDocName.setText("");txtDocspec.setText("");
        
     }
     catch(Exception e){
          JOptionPane.showMessageDialog(null, e);
     }

    }//GEN-LAST:event_btnEditActionPerformed

